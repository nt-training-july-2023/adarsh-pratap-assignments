public class ArithematicOperators {

    public static void main(String[] args) {
        
        int number1 = 200;

        int number2 = 5;

        System.out.println("Addition of 2 numbers are : " + (number2+number1));

        System.out.println("Substraction of 2 numbers are : " + (number1-number2));

        System.out.println("Multiplication of 2 numbers are : " + (number2*number1));

        System.out.println("Division of 2 numbers are : " + (number1/number2));
    }
    
}
